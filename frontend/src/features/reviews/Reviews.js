import React from "react";

const Reviews = ({ reviews }) => {
  return reviews.map((review, i) => {
    if (review.rating < 3) {
      return (
        <div key={i} style={{ paddingTop: "10px" }}>
          <div>
            <span style={{ fontWeight: "bold" }}>{review.user.name}</span>
          </div>

          <div>
            <span>{review.text}</span>
          </div>
        </div>
      );
    }
  });
};

export default Reviews;
